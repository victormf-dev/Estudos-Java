public class OperadoresLogicos {
    public static void main(String[] args) {
        boolean x = true;
        boolean y = false;
                // Operadores Lógicos são para comparação se as variáveis são verdadeiras ou falsas
        System.out.println(x && y); // && --> E - lógico --> (AND) --> 1 coisa e a outra também
        System.out.println(x || y); // || --> OU - lógico --> (OR) --> 1 coisa ou outra
        System.out.println(!x);     // ! --> Negação lógica --> (NOT) --> Não coisa (nega)
        System.out.println(!y);     // ! --> Negação lógica --> (NOT) --> Não coisa (nega)
    }
}
