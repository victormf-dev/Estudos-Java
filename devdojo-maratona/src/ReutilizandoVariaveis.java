public class ReutilizandoVariaveis {
    public static void main(String[] args) {
        double salario = 3500;
        double resultado = salario * 0.3;
        System.out.println("Trinta por Cento do salário é " + resultado);
        resultado = salario * 0.15;
        System.out.println("Quinze por Cento do salário é " + resultado);
        resultado = salario * 0.05;
        System.out.println("Cinco por Cento do salário é " + resultado);


    }
}
